<h1>  <strong>Ferias Digitales: Una gran oportunidad para<br class="hide-from" > microempresarios peruanos</strong></h1>
   
   <br>
  <p>Debido a la pandemia, muchas empresas están atravesando la peor crisis económica de su historia. Algunas tuvieron que despedir a sus empleados y cancelar todo tipo de operación comercial, pero otras aprovecharon el momento para reinventarse y seguir luchando por un ingreso económico vital para la familia y sociedad. Pero, sin duda alguna, en nuestro país y en el mundo, las más afectadas fueron las micro y pequeñas empresas. <br> <br>
Según el Instituto Nacional de Estadística e Informática (INEI, 2018), en el Perú existen un promedio de 2 millones y medio de empresas, de las cuales más del 95% están en el régimen Mype. El Instituto de Economía y Desarrollo Empresarial de la Cámara de Comercio de Lima (IEDEP- CCL, 2020) estima que el PBI caería un 3,7% para este año por efecto de la COVID-19, fundamentado en la contracción de la demanda interna privada. Las micro y pequeñas empresas (Mypes) ante este nuevo escenario son las más afectadas. <br> <br>
Conscientes de la situación, dos empresarias peruanas, asumieron el reto de crear una solución digital que sirva de puente entre la oferta de las microempresas y la demanda de un público acostumbrado a comprar sin salir de casa. Karina Higashi y Martina Anfossi, líderes de Grupo Tendenze Comunicaciones, se enfocaron en implementar este espacio digital para impulsar la venta de las pequeñas empresas y, a la vez, promover que las personas compren sin salir de casa. <br> <br>
“Era pensar rápidamente en una solución para que muchos negocios que no estaban digitalizados, logren usar este canal para vender. La idea era conseguir una vitrina donde ofrezcan sus productos y sigan generando ventas para mantener a flote sus empresas. Muchas veces las Mypes son el segmento más olvidado. El objetivo fue reactivarnos mutuamente, es decir al reactivar a las Mypes, nosotros como empresa también nos reactivamos dirigiéndonos a un nuevo mercado que necesitaba de asesoría y soporte digital a bajo precio”, explica Karina Higashi, una de las creadoras del proyecto. <br> <br>

Gracias a la experiencia que ambas peruanas emprendedoras tienen en el mundo del marketing y publicidad digital, coincidieron en que la única forma de apoyar a las Mypes durante esta etapa tan difícil era a través del medio digital.  Es así que nace la idea de crear <a href="https://feriasdigitales.pe"  target="_blank" >www.feriasdigitales.pe</a>, un espacio virtual que, bajo el concepto de una feria, reúne una variedad de marcas para promoverse en la web, Facebook e Instagram.  <br> <br>
“Iniciamos con un mes de prueba, donde brindamos el espacio gratuito a más de 30 empresas para promocionar sus productos y servicios. El objetivo fue lograr un alcance a 289,000 personas entre Facebook e Instagram. Satisfactoriamente superamos más del 300% de la meta inicial, logrando llegar a más de 700,000 personas usando estrategias pull y push.  El modelo del negocio ha sido diseñado con tarifas muy bajas, las inscripciones necesitan de poca inversión, siempre pensando en apoyar a las Mypes para que aprovechen las plataformas digitales”, informó Martina Anfossi, responsable del proyecto. <br> <br>
La ejecutiva también indicó que el 47.8% del público alcanzado fueron millennials, quienes en estos meses han demostrado su apoyo a los emprendimientos locales y buscan artículos creativos no masivos y a buen precio. Otro dato importante es que el 75,7% de público visita las ferias digitales a través de su móvil, mientras que el 23,1% a través de sus computadoras. <br> <br>
Por otro lado, Anfossi manifestó que en el primer mes proyectaron lograr 4,500 interacciones con la campaña digital, logrando superar la meta en 1,000%. Los indicadores alcanzados superaron las expectativas y muchos de los microempresarios participantes de Ferias Digitales lograron dar a conocer su marca, su oferta y también concretaron ventas interactuando con un público diverso de los distritos de San Isidro, Miraflores, Barranco, Santiago de Surco, La Molina, entre otros. <br> <br>
 <em> <strong>¿Cómo participar? </strong> </em> <br> <br>
Las empresas interesadas en formar parte de esta feria digital pueden encontrar mayor información en <a href="https://feriasdigitales.pe"  target="_blank" >www.feriasdigitales.pe</a> o en sus redes sociales  <a style="color:inherit" href="https://www.instagram.com/feriasdigitales/">@feriasdigitales</a>. Aquí encontrarán a los participantes de diversas categorías: artículos ecológicos, gastronomía, postres, belleza, cuidado de la piel, florerías, detalles, decoración, bisutería, yoga, seguros, lifestyle y otros rubros. <br> <br>
Es importante resaltar que, además de tener un espacio virtual de ventas y exhibición de sus servicios y productos, los microempresarios que se inscriban en estas ferias digitales recibirán asesoría profesional por parte de los organizadores, desde qué imágenes utilizar para promocionar sus productos hasta cómo crearse una página web. “Los micro empresarios y emprendedores son el motor de la economía del Perú. Las pequeñas empresas son las que mueven la economía y son los más afectados con la crisis actual, por eso debemos apoyarlos”, puntualizaron las organizadoras. <br>
